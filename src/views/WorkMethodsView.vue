			<div id="fizjoterapia" class="work-methods__content__block">
				<h4>Fizjoterapia</h4>
				<div class="work-methods__content__block__details">
					Fizjoterapia to zawód medyczny, którego celem jest przywrócenie,
					utrzymanie i optymalizacja ruchu i funkcjonowania osób dotkniętych
					urazem, chorobą lub niepełnosprawnością.
				</div>
				<div class="work-methods__content__block__details">
					Do kluczowych aspektów fizjoterapii należą:
					<h5>Metody leczenia</h5>
					<ol type="1">
						<li>Terapia ruchem i ćwiczeniami</li>
						<li>Terapia manualna</li>
						<li>Edukacja i doradztwo</li>
					</ol>
				</div>
				<div class="work-methods__content__block__details">
					<h5>W czym może pomóc</h5>
					<ol type="1">
						<li>
							Schorzenia układu mięśniowo-szkieletowego (np. bóle pleców,
							kontuzje sportowe)
						</li>
						<li>Zaburzenia neurologiczne (np. udar, stwardnienie rozsiane)</li>
						<li>
							Problemy sercowo-naczyniowe (np. rehabilitacja po zawale serca)
						</li>
						<li>Choroby układu oddechowego (np. astma, POChP)</li>
					</ol>
				</div>
				<div class="work-methods__content__block__details">
					<h5>Cele</h5>
					<ol type="1">
						<li>Powrót i popraw amobilności</li>
						<li>Zmniejszenie bólu</li>
						<li>Zapobieganie chorobom i dalszym urazom</li>
						<li>Promowanie ogólne zdrowie i dobrego samopoczucia</li>
					</ol>
				</div>
				<div class="work-methods__content__block__details">
					<b>Fizjoterapia przedoperacyjna</b>, polega na zaangażowaniu się w
					dostosowany program ćwiczeń przed poddaniem się operacji. Wykazano, że
					takie podejście zapewnia wiele korzyści, które mogą znacząco poprawić
					wyniki pooperacyjne. Oto kilka kluczowych punktów dotyczących
					fizjoterapii przedoperacyjnej:
				</div>
				<div class="work-methods__content__block__details">
					<h5>Korzyści z fizjoterapii przedoperacyjnej</h5>
					<ol type="1">
						<li>
							Krótszy czas regeneracji: Udział w odwyku pomaga wzmocnić i
							ujędrnić mięśnie przed operacją, co prowadzi do płynniejszego i
							szybszego procesu rekonwalescencji.
						</li>
						<li>
							Zmniejszone ryzyko powikłań: rehabilitacja przedoperacyjna może
							zwiększyć ogólną odporność i zmniejszyć prawdopodobieństwo
							powikłań pooperacyjnych, takich jak infekcje i zakrzepy krwi.
						</li>
					</ol>
				</div>
				<div class="work-methods__content__block__details">
					<h5>Leczenie bólu przed zabiegiem chirurgicznym:</h5>
					<ol type="1">
						<li>
							Fizjoterapia może pomóc złagodzić istniejący ból i zmniejszyć
							nasilenie bólu pooperacyjnego za pomocą różnych technik, takich
							jak ćwiczenia, masaż oraz zabiegi zimnem i ciepłem.
						</li>
						<li>
							Poprawiona wytrzymałość i siła: rehabilitacja przedoperacyjna
							pomaga w budowaniu ogólnej wytrzymałości i siły mięśni, co może
							złagodzić utratę siły i kontroli nad ciałem, która często
							następuje po operacji.
						</li>
						<li>
							Lepsze wyniki chirurgiczne: Pacjenci, którzy są silniejsi i
							bardziej elastyczni przed operacją, zwykle mają lepsze wyniki
							chirurgiczne, a nawet mogą odczuć oszczędności w związku ze
							zmniejszonym zapotrzebowaniem na opiekę pooperacyjną.
						</li>
						<li>
							Możliwość uniknięcia operacji: W niektórych przypadkach poprawa w
							porównaniu z okresem przed odwykiem może być tak znacząca, że
							operacja może stać się niepotrzebna.
						</li>
					</ol>
				</div>
				<div class="work-methods__content__block__details">
					<b>Fizjoterapia pooperacyjna</b>, zwana także rehabilitacją
					pooperacyjną, ma kluczowe znaczenie dla powrotu do zdrowia po różnego
					rodzaju operacjach, w tym po zabiegach ortopedycznych,
					kardiologicznych, klatki piersiowej, neurologicznych i brzusznych.
				</div>
				<div class="work-methods__content__block__details">
					Ta specjalistyczna fizjoterapia ma na celu stawienie czoła fizycznym i
					psychicznym wyzwaniom pojawiającym się po operacji oraz ułatwienie
					powrotu do normalnego funkcjonowania.
				</div>
				<div class="work-methods__content__block__details">
					<h5>Fazy rehabilitacji pooperacyjnej</h5>
					Rehabilitacja pooperacyjna zazwyczaj przebiega w trzech głównych
					fazach:
					<ol type="1">
						<li>Faza wczesnej regeneracji</li>
						<br />
						<div>
							Cel: Łagodzenie bólu i obrzęku, przywracanie ruchomości stawów i
							inicjowanie wczesnej mobilności.
						</div>
						<br />
						<div>
							Interwencje: Delikatna terapia manualna, korzystanie z pomocy do
							poruszania się (np. laski, chodziki), i proste ćwiczenia, aby
							przywrócić funkcję mięśni bez zakłócania procesu gojenia.
						</div>
						<br />
						<li>Siła i zakres fazy ruchu</li>
						<br />
						<div>
							Cel: Normalizacja mechanizmów i siły organizmu, poprawa równowagi
							i propriocepcji.
						</div>
						<br />
						<div>
							Interwencje: Bardziej wymagająca terapia manualna, ćwiczenia
							równowagi, oraz ćwiczenia zwiększające zakres ruchu i mobilności
							stawów poprzez leczenie tkanek miękkich.
						</div>
						<br />
						<li>Faza przywracania funkcjonalności</li>
