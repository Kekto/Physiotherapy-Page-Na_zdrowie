					<h5>Głowne zasady osteopatii</h5>
					<ol type="1">
						<li>
							<b>Współzależność struktury i funkcji:</b> Osteopatia opiera się
							na zasadzie, że struktura ciała (kości, mięśnie, więzadła) i
							funkcja są ze sobą ściśle powiązane. Celem osteopatów jest
							zapewnienie harmonijnego działania tych struktur w celu promowania
							ogólnego stanu zdrowia.
						</li>
						<li>
							<b>Ciało jako jednostka:</b> Osteopaci postrzegają ciało jako
							pojedynczą, zintegrowaną całość, na którą składają się ciało
							fizyczne, strefa emocjonalna i środowiskowa. Oznacza to, że biorą
							pod uwagę wszystkie czynniki przyczyniające się do objawów
							pacjenta, zamiast koncentrować się na izolowanych obszarach bólu
							lub dysfunkcji.
						</li>
						<li>
							<b>Samoleczenie i regulacja:</b> Osteopatia wspiera naturalną
							zdolność organizmu do samoleczenia i regulacji. Osteopaci
							identyfikują i usuwają blokady utrudniające proces samoleczenia,
							takie jak zła postawa, powtarzalne ruchy lub stres.
						</li>
					</ol>
				</div>
				<div class="work-methods__content__block__details">
					<h5>Holistyczne metody leczenia</h5>
					<ol type="1">
						<li>
							<b>Terapia manualna:</b> Techniki takie jak masaż, rozciąganie i
							manipulacje kręgosłupa są stosowane w celu poprawy funkcjonowania
							i łagodzenia bólu. Metody te mają na celu przywrócenie równowagi i
							prawidłowego działania organizmu.
						</li>
						<li>
							<b>Kompleksowa ocena:</b> Osteopaci przeprowadzają dokładną ocenę,
							uwzględniając historię choroby i czynniki związane ze stylem
							życia, aby zidentyfikować pierwotne przyczyny problemów
							zdrowotnych. Dzięki temu powstają spersonalizowane plany leczenia,
							dostosowane do indywidualnych potrzeb pacjenta.
						</li>
						<li>
							<b>Terapia czaszkowo-krzyżowa:</b> Delikatna technika skupiająca
							się na subtelnych ruchach czaszki i ich wpływie na zdrowie
							pacjenta.
						</li>
						<li>
							<b>Terapia wisceralna:</b> Koncentruje się na narządach
							wewnętrznych ich działaniu, i ich powiązaniach z ogólnym stanem
							zdrowia.
						</li>
						<li>
							<b>Integracje z innymi metodamy terapeutycznymi:</b> Osteopatia
							często łączy inne metody holistyczne, takie jak akupunktura,
							fizjoterapia i zmiany stylu życia. To zintegrowane podejście
							zapewnia kompleksową opiekę i uwzględnia jednocześnie wiele
							aspektów zdrowia.
						</li>
					</ol>
				</div>
