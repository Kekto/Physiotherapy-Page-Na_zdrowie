				<h4>Osteopatia</h4>
				<div class="work-methods__content__block__details">
					Osteopatia, zwana też medycyną osteopatyczną jest rodzajem manualnej
					metody leczenia ludzkiego organizmu, której początki sięgają XIX
					wieku. Jej prekursorem był wybitny amerykański lekarz/osteopata Andrew
					Taylor Still. Filozofia która przyświecała mu podczas praktyki
					lekarskiej, kierowała się odnalezieniem pierwotnej przyczyny choroby,
					naprawieniem jej i stworzeniem optymalnych warunków do leczenia dla
					organizmu. Nastawiona na traktowanie ludzkiego ciała jako integralnej
					całości, w której zachodzą przenikające się procesy. Stąd też w
					osteopatii diagnozuje się i leczy cały organizmy oraz przyczyny
					zaburzeń jego funkcjonowania, a nie tylko ich objawy. Na osteopatię
					należy patrzeć jako integralną część procesu zdrowienia do spółki z
					medycyną akademicką, a nie jej konkurencję czy wyłączną alternatywę
					poprawy stanu zdrowia.
				</div>
